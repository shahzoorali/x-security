// Helper function to get bodyguard by ID
export const getBodyguardById = (id) => {
  return mockBodyguards.find(bg => bg.id === id);
};

// Helper function to filter bodyguards
export const filterBodyguards = (filters) => {
  let filtered = [...mockBodyguards];
  
  if (filters.search) {
    const searchLower = filters.search.toLowerCase();
    filtered = filtered.filter(bg => 
      bg.name.toLowerCase().includes(searchLower) ||
      bg.skills.some(skill => skill.toLowerCase().includes(searchLower)) ||
      bg.location.toLowerCase().includes(searchLower)
    );
  }
  
  if (filters.availability && filters.availability !== 'all') {
    filtered = filtered.filter(bg => bg.availabilityStatus === filters.availability);
  }
  
  if (filters.minRating) {
    filtered = filtered.filter(bg => bg.rating >= filters.minRating);
  }
  
  if (filters.maxPrice) {
    filtered = filtered.filter(bg => bg.hourlyRate <= filters.maxPrice);
  }
  
  if (filters.isTeam !== undefined) {
    filtered = filtered.filter(bg => !!bg.isTeam === filters.isTeam);
  }
  
  return filtered;
};

